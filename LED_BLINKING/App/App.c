#include "Std_Types.h"
#include "App.h"
#include "Led.h"
#include "Gptm.h"
#include "IntCtrl.h"


/**********************************************************************************************************************
*  LOCAL MACROS CONSTANT\FUNCTION
*********************************************************************************************************************/

/**********************************************************************************************************************
 *  LOCAL DATA 
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL DATA
 *********************************************************************************************************************/
static boolean LedState=FALSE;
/**********************************************************************************************************************
 *  LOCAL FUNCTION PROTOTYPES
 *********************************************************************************************************************/
#define APPLICATION_LOGIC_CFG_ON_TIME			4 		//in seconds
#define	APPLICATION_LOGIC_CFG_OFF_TIME		2			//in seconds
static uint8 LocSeconds;
/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
/******************************************************************************
* \Syntax          : Std_ReturnType FunctionName(AnyType parameterName)        
* \Description     : Describe this service                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void Init_Drivers (void)
{
    Port_Init(&Port_ConfigPtr);
    Gpt_init(&Gpt_ConfigPtr); 
    Gpt_EnableNotification(TIMER0_A);
    Gpt_StartTimer (TIMER0_A,62500);
    IntCtrl_init();
    
 
}

/******************************************************************************
* \Syntax          : Std_ReturnType FunctionName(AnyType parameterName)        
* \Description     : Describe this service                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void App_Start (void)
{
  Enable_Exceptions();
  Init_Drivers ();
  
  if(LedState)
  {
    if(LocSeconds>=APPLICATION_LOGIC_CFG_ON_TIME)
    {
      LED_OFF(RED_LED);
      LocSeconds=0;
      LedState=FALSE;
    }
  }
  else
  {
    if(LocSeconds>=APPLICATION_LOGIC_CFG_OFF_TIME)
    {
      LED_ON(RED_LED);
      LocSeconds=0;
      LedState=TRUE;
    }
  }
}

